// Rules for creating a variable
// 1. You cannot start with number, but you can use it later(in mid or last)
// 2. You can only use underscore(_) or dollarsign($) in variable
// 3. You cannot use spaces between variable name

// variable and arithematics

var val1 = 4, val2 = 3;

//simple operations: add, subtract, multiply and divide;
console.log(val1+val2);
console.log(val1-val2);
console.log(val1*val2);
console.log(val1/val2);

//operations: power, root, etc
console.log(val1**val2); //4^3
console.log(val1**2); //4^2
console.log(val1** (1/2)); //4^(1/2)
console.log(val1** -(1/2)); //4^-(1/2)

